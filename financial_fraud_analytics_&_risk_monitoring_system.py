np.random.seed(42)

def generate_fraud_data(n_transactions=50000):
    """Generate realistic banking transaction data with fraud patterns"""

    # Generate transaction IDs and timestamps
    transaction_ids = range(1, n_transactions + 1)
    timestamps = pd.date_range('2024-01-01', periods=n_transactions, freq='5min')

    # Generate customer IDs (5000 unique customers)
    customer_ids = np.random.randint(1, 5001, n_transactions)

    # Generate amounts (normal: small-medium; fraud: often larger)
    amounts = np.where(
        np.random.random(n_transactions) < 0.05,  # 5% potentially suspicious
        np.random.exponential(5000, n_transactions),  # Larger amounts
        np.random.exponential(500, n_transactions)    # Normal amounts
    )
    amounts = np.round(amounts, 2)

    # Transaction types
    transaction_types = np.random.choice(
        ['ONLINE', 'POS', 'ATM', 'TRANSFER', 'BILL_PAYMENT'],
        n_transactions,
        p=[0.4, 0.25, 0.15, 0.15, 0.05]
    )

    # Locations (with fraud hotspots)
    locations = np.where(
        np.random.random(n_transactions) < 0.03,
        np.random.choice(['Foreign_High_Risk', 'Unknown_IP', 'Darknet_Related'], n_transactions),
        np.random.choice(['USA', 'UK', 'Canada', 'Germany', 'France'], n_transactions)
    )

    # Time features
    hours = timestamps.hour
    is_weekend = (timestamps.dayofweek >= 5).astype(int)
    is_night = ((hours < 6) | (hours > 22)).astype(int)

    # Generate fraud labels (1=fraud, 0=legitimate)
    # Fraud patterns:
    # 1. High amount + night time + foreign location
    # 2. Multiple transactions in short time
    # 3. Unusual transaction type for customer

    fraud_probability = np.zeros(n_transactions)

    # Pattern 1: High amount fraud
    mask1 = (amounts > 5000) & (is_night == 1) & (locations != 'USA')
    fraud_probability[mask1] += 0.6

    # Pattern 2: Transfer fraud
    mask2 = (transaction_types == 'TRANSFER') & (amounts > 3000)
    fraud_probability[mask2] += 0.4

    # Pattern 3: Rapid succession pattern (simulated)
    mask3 = (amounts > 2000) & (np.random.random(n_transactions) < 0.05)
    fraud_probability[mask3] += 0.5

    # Generate final fraud labels
    fraud_labels = (fraud_probability + np.random.random(n_transactions) * 0.3) > 0.7
    fraud_labels = fraud_labels.astype(int)

    # Ensure reasonable fraud rate (around 5%)
    fraud_rate = fraud_labels.mean()
    if fraud_rate > 0.08:
        fraud_labels[np.where(fraud_labels == 1)[0][:int((fraud_rate - 0.05) * n_transactions)]] = 0
    elif fraud_rate < 0.02:
        additional_fraud = np.random.choice(n_transactions, int((0.05 - fraud_rate) * n_transactions), replace=False)
        fraud_labels[additional_fraud] = 1

    # Generate customer velocity features (transactions per hour)
    velocity = np.random.poisson(2, n_transactions)
    velocity[fraud_labels == 1] = np.random.poisson(8, fraud_labels.sum())

    # Additional features
    balance_before = np.abs(np.random.normal(5000, 3000, n_transactions))
    balance_after = balance_before - amounts
    balance_after[balance_after < 0] = 0

    df = pd.DataFrame({
        'transaction_id': transaction_ids,
        'timestamp': timestamps,
        'customer_id': customer_ids,
        'amount': amounts,
        'transaction_type': transaction_types,
        'location': locations,
        'hour': hours,
        'is_weekend': is_weekend,
        'is_night': is_night,
        'velocity_1h': velocity,
        'balance_before': balance_before,
        'balance_after': balance_after,
        'is_fraud': fraud_labels
    })

    return df

