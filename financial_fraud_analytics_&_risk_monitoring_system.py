def calculate_risk_score(transaction, rf_model, scaler):
    """
    Calculate real-time fraud risk score for a transaction
    """
    # Extract features
    risk_features = np.array([[
        transaction['amount'],
        np.log1p(transaction['amount']),
        transaction['amount'] / max(transaction['balance_before'], 1),
        transaction['velocity_1h'],
        transaction['is_night'],
        transaction['is_weekend'],
        np.sin(2 * np.pi * transaction['hour'] / 24),
        np.cos(2 * np.pi * transaction['hour'] / 24),
        transaction.get('rolling_amount_mean_5', transaction['amount']),
        transaction.get('rolling_amount_std_5', 0),
        transaction.get('rolling_count_5', 1),
        transaction['transaction_type_encoded'],
        transaction['location_encoded']
    ]])

    # Scale features
    risk_features_scaled = scaler.transform(risk_features)

    # Get fraud probability
    fraud_prob = rf_model.predict_proba(risk_features_scaled)[0, 1]

    # Determine risk level
    if fraud_prob < 0.3:
        risk_level = "LOW"
        action = "Approve"
    elif fraud_prob < 0.7:
        risk_level = "MEDIUM"
        action = "Review"
    else:
        risk_level = "HIGH"
        action = "Block & Alert"

    return {
        'fraud_probability': round(fraud_prob * 100, 2),
        'risk_level': risk_level,
        'recommended_action': action
    }

