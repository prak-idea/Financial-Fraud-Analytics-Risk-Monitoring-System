class FraudAlertSystem:
 def __init__(self, threshold=0.85):
     self.threshold = threshold
     self.alerts = []

 def monitor_transaction(self, transaction, fraud_probability):
     if fraud_probability > self.threshold:
         alert = {
             'timestamp': datetime.now(),
             'customer_id': transaction.get('customer_id', 'N/A'),
             'amount': transaction.get('amount', 0),
             'fraud_probability': fraud_probability,
             'alert_level': 'CRITICAL'
         }
         self.alerts.append(alert)
         self.send_alert(alert)
         return True
     return False

 def send_alert(self, alert):
     print(f"""
     ⚠️  FRAUD ALERT ⚠️
     Time: {alert['timestamp']}
     Customer: {alert['customer_id']}
     Amount: ${alert['amount']:,.2f}
     Fraud Probability: {alert['fraud_probability']*100:.1f}%
     Action: BLOCK TRANSACTION IMMEDIATELY
     """)

 def get_alert_summary(self):
     return pd.DataFrame(self.alerts)

